    private void prepareUI() {
        typeButtonGroup.add(ingresoRadioButton);
        typeButtonGroup.add(egresoRadioButton);

        nameField.getDocument().addDocumentListener((SimpleDocumentListener) e -> {
            concepto.setNombre(nameField.getText());
        });

        valueField.getDocument().addDocumentListener((SimpleDocumentListener) e -> {
            try {
                concepto.setValor(BigDecimal.valueOf(Double.parseDouble((String) valueField.getText())));
            } catch (NumberFormatException ex) {
                concepto.setValor(BigDecimal.ZERO);
            }
        });
    }

