    private void generateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_generateButtonActionPerformed
        int mes = Integer.parseInt((String) monthComboBox.getSelectedItem());
        int anio = Integer.parseInt((String) yearComboBox.getSelectedItem());

        RolJpaController rolController = new RolJpaController(Constants.EMF);
        List<Rol> roles = rolController.findRolEntitiesByDate(anio, mes);

        BigDecimal valor = BigDecimal.ZERO;
        for (Rol rol : roles) {
            valor = valor.add(rol.calculateValue());
        }
        resultField.setText("$" + valor.setScale(2, RoundingMode.HALF_EVEN).toPlainString());
    }//GEN-LAST:event_generateButtonActionPerformed

